CREATE TABLE Payments
(
	Id UNIQUEIDENTIFIER PRIMARY KEY CLUSTERED,
	Group_Id UNIQUEIDENTIFIER FOREIGN KEY REFERENCES Groups(Id),
	Type INT NOT NULL,
	Value decimal(18,4) NOT NULL,
);

